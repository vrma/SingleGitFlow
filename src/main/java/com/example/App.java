package com.example;

/**
 * Hello world!
 *
 */
public class App 
{
    public static void main( String[] args )
    {
        System.out.println( "Hello Mundo!!" );
        
        // Tipo de datos primitivo: No tiene propiedades ni metodos
        int a = 10;
        
        // Los tipos de datos objetos, que son un envoltorio de los tipos primitivos
        // si tienen propiedades y metodos. Por cada tipo primitivo existe un tipo objeto
        // correspondiente.
        Integer b = 20;
        
        // Para almacenar un valor en un float, hay especificar un sufijo (f o F) porque de lo contrario
        // consider el valor como un double, y un double no se puede almacenar en un float
        // Estoy modificando el commit
        float c = 2.3f;
        
        
    }
}
